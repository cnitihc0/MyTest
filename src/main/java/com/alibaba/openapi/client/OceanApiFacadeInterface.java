package com.alibaba.openapi.client;

/**
 * @author hongbang.hb
 *
 */
public interface OceanApiFacadeInterface {
	/**
	 * 设置开放平台网关地址
	 * 
	 * @param host
	 *            网关地址，默认为"gw.api.alibaba.com"
	 */
	public void setServerHost(String host);

	/**
	 * 设置http端口
	 * 
	 * @param port
	 *            http端口, 默认为80
	 */
	public void setHttpPort(int port);

	/**
	 * 设置https端口
	 * 
	 * @param port
	 *            https端口，默认为443
	 */
	public void setHttpsPort(int port);

	/**
	 * 设置appKey
	 * 
	 * @param appKey
	 */
	public void setAppKey(String newAppKey);

	/**
	 * 设置签名密钥
	 * 
	 * @param secKey
	 *            签名密钥
	 */
	public void setSigningKey(String newSecKey);

	/**
	 * 
	 * @return
	 */
	public SyncAPIClient getAPIClient();
}
